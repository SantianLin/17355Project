import turtle
turtle.speed(9)
turtle.setup(600,350,200,200)
turtle.penup()
turtle.forward(-300)
turtle.pendown()
turtle.pensize(25)
turtle.color("purple")
turtle.seth(-40)
for i in range(4):
    turtle.circle(40,80)
    turtle.circle(-40,80)
turtle.circle(40,80/2)
turtle.forward(40)
turtle.circle(16,180)
turtle.forward(40*2/3)
turtle.done()
